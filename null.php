<?php

    // codigo PHP 
    // Variável do tipo Null
    // Em PHP, o tipo null representa uma variável que não possui valor atribuído. Uma variável é considerada null quando é declarada, mas não inicializada, ou quando é explicitamente definida como null.
    // Declaração de uma variável do tipo Null
    $variavelNula = null;
    // Verificando se a variável é null
    if (is_null($variavelNula)) {
        echo "A variável é do tipo Null.\n";
    } else {
        echo "A variável não é do tipo Null.\n";
    }
?>
