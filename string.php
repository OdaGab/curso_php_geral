<?php   
    // codigo PHP

    // Variável do tipo String

    // Em PHP, uma string é uma sequência de caracteres usada para representar texto. As strings podem ser definidas usando aspas simples (' ') ou aspas duplas (" ").

    // Declaração de variáveis do tipo String

    $nome = "Odair"; // Usando aspas duplas
    $sobrenome = 'Silva'; // Usando aspas simples

    // Exibindo o valor das variáveis do tipo String

    echo "Nome: " . $nome . "\n"; // Concatenando e exibindo a variável $nome
    echo "Sobrenome: " . $sobrenome . "\n"; // Concatenando e exibindo a variável $sobrenome
?>