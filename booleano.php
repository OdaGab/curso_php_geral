<?php
    // codigo PHP

    // Variável do tipo Boolean

    // O tipo booleano em PHP pode assumir apenas dois valores: true (verdadeiro) ou false (falso)
    // É comumente usado em estruturas de controle, como condicionais e loops, para determinar o fluxo do programa

    // Declaração de variáveis booleanas
    $isEstudante = true;  // Verdadeiro
    $isTrabalhando = false; // Falso

    // Exibindo o valor das variáveis booleanas
    echo "É estudante? " . ($isEstudante ? "Sim" : "Não") . "\n"; // Usando operador ternário para exibir texto legível
    echo "Está trabalhando? " . ($isTrabalhando ? "Sim" : "Não") . "\n"; // Usando operador ternário para exibir texto legível  
?>